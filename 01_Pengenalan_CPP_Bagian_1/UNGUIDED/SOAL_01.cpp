#include <iostream>
using namespace std;

int main() {
    float bilangan1;
    float bilangan2;
    cout << "Bilangan1: ";
    cin >> bilangan1;
    cout << "Bilangan2: ";
    cin >> bilangan2;

    cout << "Penjumlahan: " << bilangan1 + bilangan2 << endl;
    cout << "Pengurangan: " << bilangan1 - bilangan2 << endl;
    cout << "Perkalian: " << bilangan1 * bilangan2 << endl;
    cout << "Pembagian: " << bilangan1 / bilangan2 << endl;
}